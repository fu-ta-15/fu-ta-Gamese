#include "skillgauge.h"
#include "input_keyboard.h"
#include "manager.h"

//=============================================================================
// コンストラクタ
//=============================================================================
CSkillGauge::CSkillGauge()
{
}

//=============================================================================
// デストラクタ
//=============================================================================
CSkillGauge::~CSkillGauge()
{
}

//=============================================================================
// ゲージの生成
//=============================================================================
CSkillGauge * CSkillGauge::Create(const D3DXVECTOR3 & pos, const D3DXVECTOR2 & size, int nTex)
{
	CSkillGauge *pSkillGauge = new CSkillGauge;

	pSkillGauge->m_pos = pos;
	pSkillGauge->m_size = size;
	pSkillGauge->Init(nTex);

	return pSkillGauge;
}

//=============================================================================
// 初期化処理
//=============================================================================
HRESULT CSkillGauge::Init(int nTex)
{
	CGauge::Init(m_pos, m_size,nTex);
	CGauge::SetValue(0.075f);
	return S_OK;
}

//=============================================================================
// 終了処理
//=============================================================================
void CSkillGauge::Uninit(void)
{
	CGauge::Uninit();

}

//=============================================================================
// 更新処理
//=============================================================================
void CSkillGauge::Update(void)
{
	CGauge::Update();
}

//=============================================================================
// 描画処理
//=============================================================================
void CSkillGauge::Draw(void)
{
	CGauge::Draw();
}
