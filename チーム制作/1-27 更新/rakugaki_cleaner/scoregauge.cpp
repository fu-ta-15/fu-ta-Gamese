#include "scoregauge.h"
#include "input_keyboard.h"
#include "manager.h"

//=============================================================================
// コンストラクタ
//=============================================================================
CScoreGauge::CScoreGauge()
{
}

//=============================================================================
// デストラクタ
//=============================================================================
CScoreGauge::~CScoreGauge()
{
}

//=============================================================================
// ゲージの生成
//=============================================================================
CScoreGauge * CScoreGauge::Create(const D3DXVECTOR3 & pos, const D3DXVECTOR2 & size, int nTex)
{
	CScoreGauge *pScoreGauge = new CScoreGauge;

	pScoreGauge->m_pos = pos;
	pScoreGauge->m_size = size;
	pScoreGauge->Init(nTex);
	
	return pScoreGauge;
}

//=============================================================================
// 初期化処理
//=============================================================================
HRESULT CScoreGauge::Init(int nTex)
{
	CGauge::Init(m_pos, m_size,nTex);
	CGauge::SetValue(0.075f);

	return S_OK;
}

//=============================================================================
// 終了処理
//=============================================================================
void CScoreGauge::Uninit(void)
{
	CGauge::Uninit();
}

//=============================================================================
// 更新処理
//=============================================================================
void CScoreGauge::Update(void)
{
	CGauge::Update();
}

//=============================================================================
// 描画処理
//=============================================================================
void CScoreGauge::Draw(void)
{
	CGauge::Draw();
}
