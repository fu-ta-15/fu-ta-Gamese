//=============================================================================
// ボスの生成
//=============================================================================
CBoss * CBoss::Create(const D3DXVECTOR3 pos, const D3DXVECTOR3 size, const int nLife)
{
	CBoss *pBoss = NULL;

	if (pBoss == NULL)
	{// NULLチェック
		pBoss = new CBoss;

		pBoss->SetPos(pos);					  // 位置
		pBoss->SetSize(size);				  // サイズ
		pBoss->SetLife(nLife);
		pBoss->SetCol(WhiteColor);			  // 色
		pBoss->SetType(CEnemy::ENEMY_BOSS);	  // タイプ
		m_bBoss_Alive = true;				  // 生存する
		pBoss->Init();						  // 初期化
	}

	return pBoss;
}

